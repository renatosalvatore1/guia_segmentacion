"""Calculo métricas sobre la **forma** de la máscara binaria, no sobre los colores.
Estas métricas sirven para cuantificar si la lesión es regular, grande, redonda, etc.

- **Área:** cantidad de píxeles en la región
- **Perímetro:** largo del borde en píxeles
- **Centroide:** coordenada (fila, columna) del centro de masa
- **Circularidad:** qué tan parecida es la forma a un círculo perfecto. Vale 1 para un círculo, menos para formas irregulares. Fórmula: `4π·área / perímetro²`
- **Excentricidad:** qué tan alargada es la región (0 = círculo, 1 = línea)
"""

# === DESCRIPTORES MORFOMÉTRICOS ===

# regionprops trabaja sobre una imagen etiquetada (label)
# label() convierte la máscara bool en una imagen donde cada región tiene un número distinto
mascara_etiquetada = label(mascara_roi)
props = regionprops(mascara_etiquetada)
'''
regionprops no trabaja sobre una máscara booleana (True/False) directamente. Necesita una imagen donde cada región tenga un número único
que la identifique. label() hace exactamente eso: toma tu máscara y asigna 1 a la primera región conectada, 2 a la segunda, etc.
Por ejemplo, si tu máscara tiene la lesión principal y algunos puntitos sueltos que quedaron del Otsu, label() les asigna números
distintos. Después max(..., key=lambda r: r.area) se queda solo con la región más grande, que es la lesión real, descartando los puntitos.
'''

# Nos quedamos con la región más grande (la lesión principal)
region = max(props, key=lambda r: r.area)

# Extraemos las métricas
area        = region.area
perimetro   = region.perimeter
centroide   = region.centroid      # (fila, columna)
excentricidad = region.eccentricity

'''
Estos los calcula regionprops internamente, no tenés que hacer nada. Lo importante es entender qué significa cada uno:

area: simplemente cuántos píxeles tiene la región. Si la imagen es de 224×224 y area=974, la lesión ocupa 974 de los 50.176 píxeles totales.
perimeter: largo del borde en píxeles. Se calcula contando los píxeles de la región que tienen al menos un vecino fuera de ella.
centroid: devuelve (fila, columna) del centro de masa. Es el promedio ponderado de las posiciones de todos los píxeles de la región.
  Importante: devuelve (fila, columna) y no (x, y), por eso en el plot se usa centroide[1] para el eje horizontal y centroide[0]
  para el vertical — es un error muy común confundirlos.
eccentricity: usa la elipse equivalente de la región para calcular qué tan alargada es. Vale 0 para un círculo perfecto y se acerca a 1 cuanto más alargada es la forma.
'''

# Circularidad: 4π·área / perímetro² → 1 = círculo perfecto
circularidad = (4 * np.pi * area) / (perimetro ** 2) if perimetro > 0 else 0
'''
Esta sí la calculamos a mano porque regionprops no la trae directamente. La fórmula viene de geometría: para un círculo perfecto, área = π·r²
y perímetro = 2π·r, si sustituís obtenés que 4π·área/perímetro² vale exactamente 1. Para cualquier otra forma, el perímetro es proporcionalmente
mayor al área, entonces el resultado cae por debajo de 1. Una lesión muy irregular (como un melanoma avanzado) tendrá circularidad baja, un lunar
redondo tendrá circularidad cercana a 1.
'''

