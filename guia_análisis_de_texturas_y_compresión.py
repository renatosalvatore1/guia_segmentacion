"""Calculamos sobre los **píxeles de la ROI** (no sobre toda la imagen).
Se llaman de "primer orden" porque no tienen en cuenta la relación espacial entre píxeles,
solo la distribución de sus valores.

- **Media:** intensidad promedio → qué tan oscura/clara es la lesión
- **Desvío estándar:** dispersión → contraste interno de la lesión
- **Entropía:** complejidad/desorden del histograma → una lesión heterogénea tiene más entropía
- **Skewness (asimetría):** si la distribución se inclina hacia tonos claros (positivo) u oscuros (negativo)

Lo calculamos para los 3 canales R, G, B y los comparamos.
"""

# === DESCRIPTORES DE PRIMER ORDEN ===

def descriptores_primer_orden(canal, mascara, nombre):
    '''
    Recibe un canal de imagen, la máscara ROI y un nombre para imprimir.
    Devuelve un diccionario con las métricas.

    Separamos esto en una función porque lo vamos a llamar 3 veces (R, G, B).
    '''
    pixeles = canal[mascara].astype(float)   # solo los píxeles dentro de la ROI

    '''
    Lo primero y más importante: canal[mascara] es indexación booleana de NumPy. Cuando usás una máscara booleana como
    índice, NumPy te devuelve solo los valores donde la máscara es True, en forma de array 1D. Es decir, pixeles no es una
    imagen 2D sino una lista plana de los valores de intensidad únicamente dentro de la lesión. Esto es clave porque si calculáramos
    la media sobre toda la imagen estaríamos incluyendo el fondo negro, lo que arruinaría todas las métricas.
    '''

    media   = np.mean(pixeles)
    desvio  = np.std(pixeles)
    '''
    Estos son simples. La media te dice la intensidad promedio de la lesión en ese canal — si es baja, la lesión es oscura.
    El desvío estándar te dice cuánto varían los píxeles respecto a esa media — un desvío alto significa que hay zonas muy claras y muy
    oscuras dentro de la lesión, lo que en dermatología puede ser señal de heterogeneidad del pigmento.
    '''
    asimetria = skew(pixeles)                # scipy.stats.skew
    '''
    Skewness mide si el histograma es simétrico o se "inclina" hacia algún lado. Si es positivo, hay más píxeles oscuros y una cola hacia los claros.
    Si es negativo, hay más píxeles claros y una cola hacia los oscuros. Para una lesión pigmentada esperarías asimetría negativa en el canal azul
    (porque el pigmento absorbe el azul) y positiva en el rojo.
    '''

    # Para entropía necesitamos la distribución de probabilidad (histograma normalizado)
    hist, _ = np.histogram(pixeles, bins=256, range=(0, 256), density=True)
    hist    = hist[hist > 0]                 # eliminamos ceros para evitar log(0)
    entropia = entropy(hist)                 # scipy.stats.entropy — usa base e por defecto

    '''
    La entropía requiere un poco más de explicación. Primero np.histogram construye el histograma de probabilidades
    (density=True normaliza para que sume 1). Después hist[hist > 0] elimina los ceros — esto es necesario porque entropy
    internamente calcula p·log(p) y log(0) es infinito, lo que daría error.
    La entropía en este contexto mide cuánta variedad hay en el histograma: si todos los píxeles del ROI tienen el mismo valor, la entropía es
    0 (máximo orden). Si los valores están distribuidos uniformemente entre 0 y 255, la entropía es máxima. En términos prácticos, una lesión con
    textura compleja y variaciones de color tiene entropía alta; una zona de piel homogénea tiene entropía baja.
    '''

    print(f'  Canal {nombre}:')
    print(f'    Media:     {media:.2f}')
    print(f'    Desvío:    {desvio:.2f}')
    print(f'    Entropía:  {entropia:.4f}')
    print(f'    Skewness:  {asimetria:.4f}')

    return {'media': media, 'desvio': desvio, 'entropia': entropia, 'skewness': asimetria}

