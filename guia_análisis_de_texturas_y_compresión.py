# === JPEG ===

def comprimir_jpeg(imagen_rgb, calidad):
    '''
    Comprime una imagen RGB con JPEG a la calidad dada.
    Usa un buffer en memoria (io.BytesIO) para no guardar archivos.
    Devuelve la imagen descomprimida (para visualizar) y el tamaño en bytes.
    '''
    img_pil = Image.fromarray(imagen_rgb)

    # Guardar en memoria como JPEG
    buffer = io.BytesIO()
    img_pil.save(buffer, format='JPEG', quality=calidad)
    tamanio_bytes = buffer.tell()   # cuántos bytes ocupa el JPEG

    # Reabrir para ver cómo quedó después de la compresión
    buffer.seek(0)
    img_descomp = np.array(Image.open(buffer))

    return img_descomp, tamanio_bytes

