"""# PARTE 2 — Compresión de Imágenes

Huffman es un algoritmo de compresión **sin pérdida** basado en frecuencias:
- Los símbolos más frecuentes reciben códigos más cortos
- Los símbolos raros reciben códigos más largos

Para una imagen, los "símbolos" son los valores de píxel (0-255).
Si el nivel 200 (rosa claro de la piel) aparece muchas veces, le asignamos pocos bits.

Trabajamos sobre la imagen completa en escala de grises.
"""

# === HUFFMAN ===

def construir_arbol_huffman(frecuencias):
    '''
    Construye el árbol de Huffman a partir de un diccionario {símbolo: frecuencia}.
    Usa una cola de prioridad (heap): siempre combinamos los dos nodos de menor frecuencia.
    '''
    # Cada elemento del heap: [frecuencia, símbolo_o_None, hijo_izq, hijo_der]
    heap = [[freq, [sym, '']] for sym, freq in frecuencias.items()]
    heapq.heapify(heap)
    '''
    Primero construimos una lista de nodos, uno por cada nivel de gris que aparece en la imagen. Cada nodo tiene esta forma:
    [frecuencia, [símbolo, código_acumulado]]
    Por ejemplo para el nivel 200 con frecuencia 1400:
    [1400, [200, '']]

    El código empieza vacío '' porque todavía no sabemos qué bits le tocan.
    heapq.heapify convierte la lista en un heap mínimo: una estructura donde siempre podés sacar el elemento de menor frecuencia en O(1).
    Es como una fila de prioridad donde el más "barato" (menos frecuente) sale primero.
    '''

    while len(heap) > 1:
        # Tomamos los dos nodos de menor frecuencia
        lo = heapq.heappop(heap)
        hi = heapq.heappop(heap)
        # Asignamos '0' al de menor frecuencia y '1' al de mayor
        for par in lo[1:]:
            par[1] = '0' + par[1]
        for par in hi[1:]:
            par[1] = '1' + par[1]
        # Los combinamos en un nuevo nodo con frecuencia sumada
        heapq.heappush(heap, [lo[0] + hi[0]] + lo[1:] + hi[1:])

    '''
    Este es el corazón del algoritmo. En cada iteración:

    Sacamos los dos nodos de menor frecuencia (lo y hi)
    Al de menor frecuencia (lo) le agregamos '0' adelante de su código
    Al otro (hi) le agregamos '1' adelante
    Los combinamos en un nuevo nodo cuya frecuencia es la suma de ambas, y lo volvemos a meter al heap

    Esto se repite hasta que queda un solo nodo — la raíz del árbol. Al final cada símbolo tiene acumulado su código completo de 0s y 1s.
    Un ejemplo con 4 niveles simplificado:
    Inicio:  [1, A]  [3, B]  [5, C]  [8, D]

    Paso 1: combinamos A(1) y B(3) → nuevo nodo AB(4)
            A queda con '0', B con '1'

    Paso 2: combinamos AB(4) y C(5) → nuevo nodo ABC(9)
            A queda con '00', B con '10', C con '1'

    Paso 3: combinamos ABC(9) y D(8) → raíz(17)
            A='000', B='100', C='10', D='1'
    Los más frecuentes (D, C) tienen códigos más cortos. Los raros (A) tienen códigos largos.
    '''

    return dict(sorted(heapq.heappop(heap)[1:], key=lambda x: x[0]))

def calcular_compresion_huffman(imagen_gray):
    '''
    Calcula los códigos Huffman y estima cuántos bits necesitaría la imagen comprimida.
    No genera el archivo binario (eso requeriría manejo de bits), pero calcula la reducción.
    '''
    # Contar frecuencia de cada nivel de intensidad
    pixeles_flat = imagen_gray.flatten()
    frecuencias  = Counter(pixeles_flat.tolist())
    '''
    flatten() convierte la imagen 2D de 224×224 en un array 1D de 50.176 valores.
    Counter cuenta cuántas veces aparece cada valor — devuelve algo como {200: 1400, 185: 1200, 50: 3, ...}.
    '''

    # Construir árbol y obtener códigos
    codigos = construir_arbol_huffman(frecuencias)
    '''
    Llamamos a la función anterior y obtenemos un diccionario como:
    {200: '10', 185: '110', 50: '0001101', ...}
    '''

    # Calcular bits totales con Huffman
    bits_huffman = sum(frecuencias[s] * len(codigos[s]) for s in frecuencias)

    # Bits originales: 8 bits por píxel (256 niveles)
    bits_original = len(pixeles_flat) * 8

    ratio = bits_huffman / bits_original
    return codigos, bits_original, bits_huffman, ratio
    '''
    frecuencias[s] → cuántas veces aparece el símbolo s
    len(codigos[s]) → cuántos bits tiene su código Huffman
    Multiplicamos ambos y sumamos para todos los símbolos → total de bits que necesitaría la imagen comprimida
    bits_original es simplemente n_pixeles × 8 porque sin compresión cada píxel usa 8 bits fijos
    ratio menor a 1 significa que Huffman usa menos bits que el original
    '''

codigos_huff, bits_orig, bits_huff, ratio_huff = calcular_compresion_huffman(gray)

