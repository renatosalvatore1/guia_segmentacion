"""RLE es el algoritmo de compresión **sin pérdida** más simple:
en vez de guardar cada píxel individualmente, guarda **pares (valor, cantidad)**.

Ejemplo: `[255, 255, 255, 100, 100]` → `[(255, 3), (100, 2)]`

Funciona bien cuando hay grandes zonas uniformes (fondos, máscaras binarias).
La guía pide aplicarlo en **dos direcciones**: horizontal (fila a fila) y vertical (columna a columna),
para comparar cuál da mejor compresión según la estructura de la imagen.
"""

# === RLE ===

def rle_codificar(secuencia):
    '''
    Aplica RLE a un array 1D.
    Devuelve lista de (valor, cantidad) y la cantidad total de pares.
    '''
    if len(secuencia) == 0:
        return [], 0

    codificado = []
    valor_actual = secuencia[0]
    contador     = 1

    for pixel in secuencia[1:]:
        if pixel == valor_actual:
            contador += 1
        else:
            codificado.append((valor_actual, contador))
            valor_actual = pixel
            contador     = 1

    codificado.append((valor_actual, contador))  # último grupo
    return codificado, len(codificado)

def rle_imagen(imagen, direccion='horizontal'):
    '''
    Aplica RLE a una imagen completa fila por fila (horizontal)
    o columna por columna (vertical).
    Devuelve el total de pares (menor = mejor compresión).
    '''
    total_pares = 0

    if direccion == 'horizontal':
        # Recorremos cada fila
        for fila in imagen:
            _, n_pares = rle_codificar(fila)
            total_pares += n_pares
    elif direccion == 'vertical':
        # Transponemos y recorremos cada "fila" (que son columnas originales)
        for columna in imagen.T:
            _, n_pares = rle_codificar(columna)
            total_pares += n_pares

    return total_pares

